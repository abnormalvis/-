// 改进的显示子系统
#define REFRESH_STRIDE 8      // 每次刷新8列
static uint8_t refresh_x = 0; // 刷新起始列

void OLED_Task()
{
    static uint32_t last_refresh;             // 上次刷新时间
    uint8_t col, start_col, end_col, x, page; // 列数，起始列，结束列，x坐标，页数
    last_refresh = 0;                         // 初始化上次刷新时间

    // 按50Hz， 也就是20ms的间隔来局部刷新
    if (SystemTick - last_refresh < 20)
    { // 检测刷新间隔
        return;
    }
    last_refresh = SystemTick; // 更新上次刷新时间

    // 分块刷新逻辑
    start_col = refresh_x; // 计算起始列和结束列
    end_col = (refresh_x + REFRESH_STRIDE) % 128;

    OLED_SetPos(start_col, 0); // 设置起始位置
    for (x = start_col; x < end_col; x++)
    { // 遍历每一行
        for (page = 0; page < 8; page++)
        { // 遍历每一页
            // 写入数据
            OLED_WriteData(OLEDBuffer[display_index][x + page * SCREEN_WIDTH]);
        }
    }

    refresh_x = end_col; // 更新最后的结束列

    // 重要信息立即刷新
    if (score_updated)
    {                       // 如果分数更新，则立即刷新
        OLED_SetPos(80, 0); // 设置起始列=80，页=0
        for (col = 80; col < 128; col++)
        { // 48像素宽（128-80=48）
            for (page = 0; page < 8; page++)
            {
                OLED_WriteData(OLEDBuffer[display_index][col + page * SCREEN_WIDTH]);
            }
        }
        score_updated = true;
    }
}

static void OLED_I2C_Start()
{
    OLED_SDA = 1;
    _nop_();
    OLED_SCL = 1;
    _nop_();
    OLED_SDA = 0;
    _nop_();
    OLED_SCL = 0;
    _nop_();
}

// I2C停止信号
static void OLED_I2C_Stop()
{
    OLED_SDA = 0;
    _nop_();
    OLED_SCL = 1;
    _nop_();
    OLED_SDA = 1;
    _nop_();
}

// 发送一个字节（从高位开始）
static void OLED_I2C_SendByte(uint8_t dat)
{
    uint8_t i;
    for (i = 0; i < 8; i++)
    {
        OLED_SDA = (dat & 0x80) ? 1 : 0;
        OLED_SCL = 1;
        _nop_(); // 时钟上升沿
        OLED_SCL = 0;
        _nop_();
        dat <<= 1;
    }
    // 等待ACK
    OLED_SDA = 1;
    _nop_();
    OLED_SCL = 1;
    _nop_();
    OLED_SCL = 0;
    _nop_();
}

/*---- 命令/数据发送 ----*/
// 发送控制命令
static void OLED_WriteCmd(uint8_t cmd)
{
    OLED_I2C_Start();
    OLED_I2C_SendByte(0x78); // 设备地址+写模式
    OLED_I2C_SendByte(0x00); // 控制字节（命令）
    OLED_I2C_SendByte(cmd);
    OLED_I2C_Stop();
}

// 发送显示数据
static void OLED_WriteData(uint8_t dat)
{
    OLED_I2C_Start();
    OLED_I2C_SendByte(0x78); // 设备地址+写模式
    OLED_I2C_SendByte(0x40); // 控制字节（数据）
    OLED_I2C_SendByte(dat);
    OLED_I2C_Stop();
}

// 初始化序列
void OLED_Init(void)
{
    Delay(100); // 等待电源稳定

    OLED_WriteCmd(0xAE); // 关闭显示

    OLED_WriteCmd(0xD5); // 设置时钟分频
    OLED_WriteCmd(0x80);

    OLED_WriteCmd(0xA8); // 设置多路复用率
    OLED_WriteCmd(0x3F);

    OLED_WriteCmd(0xD3); // 设置显示偏移
    OLED_WriteCmd(0x00);

    OLED_WriteCmd(0x40); // 设置起始行

    OLED_WriteCmd(0x8D); // 电荷泵设置
    OLED_WriteCmd(0x14);

    OLED_WriteCmd(0x20); // 内存地址模式
    OLED_WriteCmd(0x00);

    OLED_WriteCmd(0xA1); // 段重映射
    OLED_WriteCmd(0xC8); // 扫描方向

    OLED_WriteCmd(0xDA); // COM引脚配置
    OLED_WriteCmd(0x12);

    OLED_WriteCmd(0x81); // 对比度设置
    OLED_WriteCmd(0xCF);

    OLED_WriteCmd(0xD9); // 预充电周期
    OLED_WriteCmd(0xF1);

    OLED_WriteCmd(0xDB); // VCOMH电平
    OLED_WriteCmd(0x40);

    OLED_WriteCmd(0xA4); // 显示全部打开
    OLED_WriteCmd(0xA6); // 正常显示

    OLED_Clear();
    OLED_WriteCmd(0xAF); // 开启显示
}

// 清屏（同时清空显存）
void OLED_Clear(void)
{
    memset(oled_buffer, 0, sizeof(oled_buffer));
    OLED_Refresh();
}

// 设置显示坐标
void OLED_SetPos(uint8_t x, uint8_t y)
{
    OLED_WriteCmd(0xB0 + y);                 // 设置页地址
    OLED_WriteCmd(((x & 0xF0) >> 4) | 0x10); // 列地址高4位
    OLED_WriteCmd(x & 0x0F);                 // 列地址低4位
}

// 刷新显存到屏幕
void OLED_Refresh(void)
{
    uint8_t page, col;
    for (page = 0; page < 8; page++)
    {
        OLED_WriteCmd(0xB0 + page); // 设置页地址
        OLED_WriteCmd(0x00);        // 列低地址
        OLED_WriteCmd(0x10);        // 列高地址

        // 发送整页数据
        for (col = 0; col < 128; col++)
        {
            OLED_WriteData(oled_buffer[page][col]);
        }
    }
}

// 画点函数
void OLED_DrawPoint(uint8_t x, uint8_t y, uint8_t mode)
{
    uint8_t page;    // 页号
    uint8_t bit_pos; // 位位置
    if (x >= SCREEN_WIDTH || y >= SCREEN_HEIGHT)
        return;

    page = y / 8;
    bit_pos = y % 8;

    if (mode)
        oled_buffer[page][x] |= (1 << bit_pos); // 画白点
    else
        oled_buffer[page][x] &= ~(1 << bit_pos); // 画黑点
}

// 显示ASCII字符（8x16）
void OLED_ShowChar(uint8_t x, uint8_t y, char chr)
{
    uint8_t c, i, line, j;
    c = chr - ' ';
    if (x > SCREEN_WIDTH - 8 || y > SCREEN_HEIGHT - 16)
        return;

    for (i = 0; i < 8; i++)
    {
        line = F8X16[c * 16 + i];
        for (j = 0; j < 8; j++)
        {
            OLED_DrawPoint(x + i, y + j, (line >> j) & 0x1);
        }

        line = F8X16[c * 16 + i + 8];
        for (j = 0; j < 8; j++)
        {
            OLED_DrawPoint(x + i, y + j + 8, (line >> j) & 0x1);
        }
    }
}

// 显示字符串
void OLED_ShowString(uint8_t x, uint8_t y, const char *str)
{
    while (*str)
    {
        OLED_ShowChar(x, y, *str++);
        x += 8;
        if (x > SCREEN_WIDTH - 8)
        {
            x = 0;
            y += 16;
        }
    }
}

// 绘制位图
void OLED_DrawBMP(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1, const uint8_t BMP[])
{
    uint32_t index = 0;
    uint8_t x, y;
    for (y = y0; y <= y1; y++)
    {
        for (x = x0; x <= x1; x++)
        {
            oled_buffer[y][x] = BMP[index++];
        }
    }
}


void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len)
{
    char buf[10];
    uint8_t i;
    
    // 从高位到低位填充数字
    for(i = 0; i < len; i++) {
        buf[len - 1 - i] = (num % 10) + '0'; // 取最后一位
        num /= 10;
    }
    buf[len] = '\0'; // 字符串结尾
    
    OLED_ShowString(x, y, buf);
}

